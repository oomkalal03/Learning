
public class switchChallenge {
    public static void main(String[] args) {
        char variable= 'A';
        switch (Character.toUpperCase(variable)){
            case 'A':
                System.out.println("It is a vowel");
                break;
            case 'E':
                System.out.println("It is a vowel");
                break;
            case 'I':
                System.out.println("It is a vowel");
                break;
            case 'O':
                System.out.println("It is a vowel");
                break;
            case 'U':
                System.out.println("It is a vowel");
                break;
            default:
                System.out.println("It is not a vowel");
                break;
        }

    }

}
